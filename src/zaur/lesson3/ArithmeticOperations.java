package zaur.lesson3;

public class ArithmeticOperations {

    public static void main(String[] args) {
        int i1 = 5;
        System.out.println(i1); // 5

        i1 += 3;
        System.out.println(i1); // 8
        i1 -= 2;
        System.out.println(i1); // 6
        i1 /= 3;
        System.out.println(i1); // 2
        i1 *= 4;
        System.out.println(i1); // 8
    }
}
